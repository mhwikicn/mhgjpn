function CalcSkill()
{
  for (i=0; i<65; i++) {
    skillGen[i] = 99;
    skillAta[i] = 0;
    skillDou[i] = 0;
    skillUde[i] = 0;
    skillKos[i] = 0;
    skillAsi[i] = 0;
    skillDov[i] = 0;
  }

  for (i=0; i<5; i++) {
    if (selFlag[i] >= 0) {
      str = bugu[selFlag[i]];
      for (j=0; j<30; j+=5) {
        n1 = Number(str.substring(j,j+2));
        if (n1 > 0) {
          n2 = Number(str.substring(j+2,j+5));
          if (skillGen[n1] == 99) {
            skillGen[n1] = n2;
          } else {
            skillGen[n1] += n2;
          }
        }
        if (i == 0) {
          skillAta[n1] += n2;
        } else if (i == 1) {
          skillDou[n1] += n2;
        } else if (i == 2) {
          skillUde[n1] += n2;
        } else if (i == 3) {
          skillKos[n1] += n2;
        } else {
          skillAsi[n1] += n2;
        }
      }
    }
  }
  for (i=0; i<65; i++) {
    if (skillGen[i] != 99) {
      if (skillAta[i] > 0) {
        if (i == 41)  AddDouVal(1);
        if (i == 42)  AddDouVal(2);
        if (i == 43)  DblDouVal();
      }
      if (skillUde[i] > 0) {
        if (i == 41)  AddDouVal(1);
        if (i == 42)  AddDouVal(2);
        if (i == 43)  DblDouVal();
      }
      if (skillKos[i] > 0) {
        if (i == 41)  AddDouVal(1);
        if (i == 42)  AddDouVal(2);
        if (i == 43)  DblDouVal();
      }
      if (skillAsi[i] > 0) {
        if (i == 41)  AddDouVal(1);
        if (i == 42)  AddDouVal(2);
        if (i == 43)  DblDouVal();
      }
    }
  }
}


